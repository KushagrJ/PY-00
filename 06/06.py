dayOfWeek = int(input("Enter an integer value between 1 and 7: "))

if (dayOfWeek == 1):
    print("The first day of the week is Monday\n", sep = "", end = "")
elif (dayOfWeek == 2):
    print("The second day of the week is Tuesday\n", sep = "", end = "")
elif (dayOfWeek == 3):
    print("The third day of the week is Wednesday\n", sep = "", end = "")
elif (dayOfWeek == 4):
    print("The fourth day of the week is Thursday\n", sep = "", end = "")
elif (dayOfWeek == 5):
    print("The fifth day of the week is Friday\n", sep = "", end = "")
elif (dayOfWeek == 6):
    print("The sixth day of the week is Saturday\n", sep = "", end = "")
elif (dayOfWeek == 7):
    print("The seventh day of the week is Sunday\n", sep = "", end = "")
else:
    print("Enter an integer value between 1 and 7!\n", sep = "", end = "")





