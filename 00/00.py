# -*- coding: utf-8 -*-
# Python 3.8.6

print("Hello, World!\n", sep = "", end = "")



# /* Trivia
#
#  * \n represents the newline character.
#  * The print function in Python automatically prints spaces between its arguments, when separated by commas. To avoid
#    this, sep = "" is used. Similarly, print also automatically prints newline characters after each print function. To
#    avoid this, end = "" is used.
#
#  */